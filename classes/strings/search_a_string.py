# 'in' keyword is used to search a string
# term in string -> searching for 'term' in the 'string'
# term in string returns True
# term not in string returns False

quote = "Where there is a will there is a way."
print('here' in quote)      # True
print('ill' in quote)       # True
print('bill' in quote)      # False
print('bill' not in quote)  # True