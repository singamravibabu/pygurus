# replace(old, new[, num])
phone_number = "99 88 123 456"
print(phone_number.replace(" ", ""))       # '9988123456'

phone_number = phone_number.replace(" ", "")
print(phone_number)

cc_card = "1234-5678-9101-1123"
print(cc_card.replace("-", ""))     # '1234567891011123'

cc_card = cc_card.replace("-", "")
print(cc_card)                  # '1234567891011123'