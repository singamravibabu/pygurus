# join() method joins a sequence using
# 'text'.join(sequence)

stationary_items = ["pen", "paper", "colors", "book", "charts", "maps"]
joined_items = ', '.join(stationary_items)
print(joined_items)

joined_items = '-'.join(stationary_items)
print(joined_items)

