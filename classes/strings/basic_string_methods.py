# isalpha()
str1 = "hello world"
str2 = "hello"
print(str1.isalpha())
print(str2.isalpha())
print()

# islower()
str3 = "hello world! 123"
str4 = "Hello"
print(str3.islower())
print(str4.islower())
print()

# isupper()
str5 = "GOOD MORNING!!!"
str6 = "GOOD MORNINg"
print(str5.isupper())
print(str6.islower())
print()

