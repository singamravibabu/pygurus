from datetime import date, time, datetime

sample_dt = datetime(2024, 11, 26, 10, 15, 40, 100)

# year - returns year
print(sample_dt.year)

# month - returns month
print(sample_dt.month)

# day- returns day
print(sample_dt.day)

# hour - returns hour
print(sample_dt.hour)

# minute - returns minute
print(sample_dt.minute)

# second - returns second
print(sample_dt.second)

# microsecond - microsecond
print(sample_dt.microsecond)